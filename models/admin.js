import { model, Schema } from "mongoose";
const adminSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    surname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, { versionKey: false })

const adminModel = model('admin', adminSchema)
export default adminModel
